md"""
# Graph
**Graph** ``G`` consists of two sets, a vertex set ``V`` and an edge set ``E``, together with two functions ``src , tgt : E \rightarrow V``. Every edge ``e : E`` in the graph has a **source** vertex and a **target** vertex.

## Math notation

``E : Type \dashv``

``V : Type \dashv``

``src(e) : V \dashv e : E``

``tgt(e) : V \dashv e : E``

## GATlab notation
```julia
@theory ThGraph begin
  V::TYPE ⊣ []
  E::TYPE ⊣ []

  src(edge)::V ⊣ [edge::E]
  tgt(edge)::V ⊣ [edge::E]
end
```

## Catlab notation
This is called a GAT (schema?) presentation.
```julia
@present SchGraph(FreeSchema) begin
    V::Ob
	E::Ob
	src::Hom(E,V)
	tgt::Hom(E,V)
  end
```

And this incantation finally defines the graph type. Lucky us, it's already defined in the categorical algebra library, so we can just watch for now.
```julia
@acset_type Graph(SchGraph, index=[:src,:tgt])
```

### Example
Let's create a graph using this interface.
"""

